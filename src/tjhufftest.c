#include <string.h>
#include <setjmp.h>
#include "jpeglib.h"

static int test_count = 0;
static int pass_count = 0;

/* Error handler for libjpeg */
struct my_error_mgr {
  struct jpeg_error_mgr pub;
  jmp_buf setjmp_buffer;
};

static void my_error_exit(j_common_ptr cinfo)
{
  struct my_error_mgr *myerr = (struct my_error_mgr *)cinfo->err;
  longjmp(myerr->setjmp_buffer, 1);
}

static void my_emit_message(j_common_ptr cinfo, int msg_level)
{
  (void)cinfo;
  (void)msg_level;
}


/*
 * Try to decode a JPEG from memory. Returns 1 if completed without crash.
 * With AddressSanitizer enabled, OOB reads would be caught here.
 */
static int try_decode(const unsigned char *data, size_t size, const char *name)
{
  struct jpeg_decompress_struct cinfo;
  struct my_error_mgr jerr;
  JSAMPARRAY buffer = NULL;
  int row_stride;

  test_count++;
  printf("  Test %d: %-45s ", test_count, name);
  fflush(stdout);

  cinfo.err = jpeg_std_error(&jerr.pub);
  jerr.pub.error_exit = my_error_exit;
  jerr.pub.emit_message = my_emit_message;

  if (setjmp(jerr.setjmp_buffer)) {
    jpeg_destroy_decompress(&cinfo);
    printf("PASS (error handled)\n");
    pass_count++;
    return 1;
  }

  jpeg_create_decompress(&cinfo);
  jpeg_mem_src(&cinfo, data, (unsigned long)size);

  if (jpeg_read_header(&cinfo, TRUE) != JPEG_HEADER_OK) {
    jpeg_destroy_decompress(&cinfo);
    printf("PASS (header rejected)\n");
    pass_count++;
    return 1;
  }

  if (!jpeg_start_decompress(&cinfo)) {
    jpeg_destroy_decompress(&cinfo);
    printf("PASS (decompress rejected)\n");
    pass_count++;
    return 1;
  }

  row_stride = cinfo.output_width * cinfo.output_components;
  buffer = (*cinfo.mem->alloc_sarray)((j_common_ptr)&cinfo, JPOOL_IMAGE,
                                       row_stride, 1);

  while (cinfo.output_scanline < cinfo.output_height) {
    jpeg_read_scanlines(&cinfo, buffer, 1);
  }

  jpeg_finish_decompress(&cinfo);
  jpeg_destroy_decompress(&cinfo);

  printf("PASS (decoded ok)\n");
  pass_count++;
  return 1;
}


